/**
 * Deterministic Nucleo icon codegen.
 *
 * Reads packages/canvas/src/ui/icons/manifest.json and emits:
 *   1. packages/canvas/src/ui/icons/nucleo/<kebab>-icon.tsx — one React chrome
 *      component per icon, plus a nucleo/index.ts barrel re-exporting them all.
 *   2. packages/canvas/src/objects/shapes/icon/icon-glyph-data.generated.ts —
 *      a single serializable glyph-data registry (consumed whole). Canvas
 *      CONTENT, so it co-locates with the icon object def, not with the
 *      interface icons (co-location alignment).
 *
 * Run: bun tools/nucleo-icons/generate.ts
 *
 * Source SVGs are resolved against the canonical Nucleo library, discovered in
 * this order:
 *   (a) --library <dir> CLI flag
 *   (b) NUCLEO_LIBRARY_DIR env var
 *   (c) the default Dropbox library path
 *   (d) the vendored copy under nucleo/svg/ (self-sufficient on CI / other
 *       machines where the library is absent)
 * Every SVG resolved from the library is vendored byte-verbatim into
 * nucleo/svg/ (source path "/" → "--") so the vendored copy alone can
 * regenerate identical output. The generator owns the nucleo/ directory: it
 * rewrites every .tsx/.ts it emits and prunes vendored SVGs no longer
 * referenced by the manifest.
 *
 * No dependencies beyond node/bun builtins. The Nucleo corpus is
 * machine-generated and regular, so SVGs are parsed with a small tokenizer
 * rather than a full XML parser. All validation problems are collected and
 * reported together before exiting non-zero.
 */

