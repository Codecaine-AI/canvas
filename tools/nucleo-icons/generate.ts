function loadManifest(): Manifest | null {
  if (!existsSync(MANIFEST_PATH)) {
    fail(`manifest not found at ${MANIFEST_PATH}`);
    return null;
  }
  try {
    return JSON.parse(readFileSync(MANIFEST_PATH, "utf8")) as Manifest;
  } catch (e) {
    fail(`manifest is not valid JSON: ${(e as Error).message}`);
    return null;
  }
}

function main(): void {
  const manifest = loadManifest();
  if (manifest === null) {
    report(0, 0, 0);
    return;
  }
  const chrome = manifest.chrome ?? [];
  const glyphs = manifest.glyphs ?? [];

  ensureDirs();

  // --- Validate manifest shape / duplicates -------------------------------
  const seenExports = new Set<string>();
  for (const entry of chrome) {
    if (typeof entry.export !== "string" || !/^[A-Za-z_$][\w$]*$/.test(entry.export)) {
      fail(`chrome entry has invalid export name: ${JSON.stringify(entry.export)}`);
      continue;
    }
    if (seenExports.has(entry.export)) fail(`duplicate chrome export name "${entry.export}"`);
    seenExports.add(entry.export);
    if (!entry.custom && typeof entry.source !== "string") {
      fail(`chrome entry "${entry.export}" is not custom but has no source`);
    }
  }
  const seenIds = new Set<string>();
  for (const entry of glyphs) {
    if (typeof entry.id !== "string" || entry.id.length === 0) {
      fail(`glyph entry has invalid id: ${JSON.stringify(entry.id)}`);
      continue;
    }
    if (seenIds.has(entry.id)) fail(`duplicate glyph id "${entry.id}"`);
    seenIds.add(entry.id);
    if (typeof entry.source !== "string") {
      fail(`glyph entry "${entry.id}" has no source`);
    }
  }

  // --- Chrome components (one module per icon) -----------------------------
  const chromeSorted = [...chrome]
    .filter((e) => !e.custom && typeof e.source === "string")
    .sort((a, b) => (a.export < b.export ? -1 : a.export > b.export ? 1 : 0));

  // { exportName, fileBase } for the barrel, in export order.
  const modules: { exportName: string; fileBase: string; text: string }[] = [];
  for (const entry of chromeSorted) {
    const svg = loadSource(entry.source!);
    if (svg === null) continue;
    const text = emitTsxModule(entry.export, svg, entry.source!);
    if (text !== null) {
      modules.push({ exportName: entry.export, fileBase: kebabCase(entry.export), text });
    }
  }

  // --- Glyph data -----------------------------------------------------------
  const glyphBlocks: string[] = [];
  const glyphsSorted = [...glyphs]
    .filter((e) => typeof e.source === "string")
    .sort((a, b) => (a.id < b.id ? -1 : a.id > b.id ? 1 : 0));
  for (const entry of glyphsSorted) {
    const svg = loadSource(entry.source);
    if (svg === null) continue;
    const context = `${entry.source} (glyph "${entry.id}")`;
    const converted: GlyphElementOut[] = [];
    let ok = true;
    for (const el of svg.elements) {
      const out = convertGlyphElement(el, context);
      if (out === null) ok = false;
      else converted.push(out);
    }
    if (!ok) continue;
    glyphBlocks.push(
      [
        `  // ${entry.source}`,
        `  ${JSON.stringify(entry.id)}: {`,
        `    viewBoxSize: ${svg.viewBoxSize},`,
        "    elements: [",
        ...converted.map((el) => `      ${emitGlyphElement(el)},`),
        "    ],",
        "  },",
      ].join("\n"),
    );
  }

  if (errors.length > 0) {
    report(0, 0, 0);
    return;
  }

  // --- Write outputs --------------------------------------------------------
  clearGeneratedModules();

  for (const mod of modules) {
    writeFileSync(resolve(NUCLEO_DIR, `${mod.fileBase}.tsx`), mod.text, "utf8");
  }

  const barrel = [
    GENERATED_HEADER,
    "",
    'export type { IconProps } from "../icon-props";',
    "",
    ...modules.map((mod) => `export { ${mod.exportName} } from "./${mod.fileBase}";`),
    "",
  ].join("\n");
  writeFileSync(resolve(NUCLEO_DIR, "index.ts"), barrel, "utf8");

  const glyphOut = [
    GENERATED_HEADER,
    "",
    "// Structural twin of IconGlyphElement in ./icon-glyphs.ts (kept local to",
    "// avoid an import cycle — the shapes must stay assignment-compatible).",
    "type IconGlyphElement =",
    '  | { kind: "path"; d: string }',
    '  | { kind: "circle"; cx: number; cy: number; r: number }',
    '  | { kind: "line"; x1: number; y1: number; x2: number; y2: number };',
    "",
    "export const GENERATED_ICON_GLYPH_ELEMENTS: Record<",
    "  string,",
    "  { viewBoxSize: number; elements: readonly IconGlyphElement[] }",
    "> = {",
    glyphBlocks.join("\n"),
    "};",
    "",
  ].join("\n");
  writeFileSync(GLYPH_OUT_PATH, glyphOut, "utf8");

  pruneVendored();

  report(modules.length, glyphBlocks.length, vendoredThisRun.size);
}

function report(componentCount: number, glyphCount: number, vendoredCount: number): void {
  if (errors.length > 0) {
    console.error(`nucleo icons: ${errors.length} error(s):`);
    for (const e of errors) console.error(`  - ${e}`);
    process.exit(1);
  }
  console.log(`nucleo icons: wrote ${componentCount} chrome component module(s) → ${NUCLEO_DIR}`);
  console.log(`nucleo icons: wrote ${glyphCount} glyph entr(ies) → ${GLYPH_OUT_PATH}`);
  console.log(`nucleo icons: vendored ${vendoredCount} distinct source SVG(s) → ${VENDOR_DIR}`);
}

main();
