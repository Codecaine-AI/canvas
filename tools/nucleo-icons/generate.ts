/** Source paths that were successfully vendored this run (for pruning). */
const vendoredThisRun = new Set<string>();

/**
 * Return the raw SVG text for a manifest source, resolving it against the
 * library roots (copying byte-verbatim into the vendored dir) and falling back
 * to the vendored copy. Records an error and returns null if unresolvable.
 */
function loadSourceText(source: string): string | null {
  const vendorPath = resolve(VENDOR_DIR, vendorName(source));

  for (const root of LIBRARY_ROOTS) {
    const abs = resolve(root, source);
    if (existsSync(abs)) {
      const bytes = readFileSync(abs);
      // Only rewrite the vendored file if its bytes differ, keeping output
      // deterministic and avoiding needless churn.
      if (!existsSync(vendorPath) || !readFileSync(vendorPath).equals(bytes)) {
        writeFileSync(vendorPath, bytes);
      }
      vendoredThisRun.add(source);
      return bytes.toString("utf8");
    }
  }

  if (existsSync(vendorPath)) {
    vendoredThisRun.add(source);
    return readFileSync(vendorPath, "utf8");
  }

  fail(
    `${source}: source not found in the Nucleo library (checked ${LIBRARY_ROOTS.join(
      ", ",
    )}) nor in the vendored copy at ${vendorPath}`,
  );
  return null;
}

function loadSource(source: string): ParsedSvg | null {
  const text = loadSourceText(source);
  if (text === null) return null;
  return parseSvg(source, text);
}

// ---------------------------------------------------------------------------
// Directory ownership (deterministic state of nucleo/)
// ---------------------------------------------------------------------------

/** Ensure nucleo/ and nucleo/svg/ exist. */
function ensureDirs(): void {
  mkdirSync(VENDOR_DIR, { recursive: true });
}

/** Delete every .tsx/.ts directly under nucleo/ (the generator owns them). */
function clearGeneratedModules(): void {
  if (!existsSync(NUCLEO_DIR)) return;
  for (const name of readdirSync(NUCLEO_DIR)) {
    if (name.endsWith(".tsx") || name.endsWith(".ts")) {
      rmSync(resolve(NUCLEO_DIR, name));
    }
  }
}

/** Prune vendored SVGs no longer referenced by the manifest. */
function pruneVendored(): void {
  if (!existsSync(VENDOR_DIR)) return;
  const keep = new Set([...vendoredThisRun].map(vendorName));
  for (const name of readdirSync(VENDOR_DIR)) {
    if (name.endsWith(".svg") && !keep.has(name)) {
      rmSync(resolve(VENDOR_DIR, name));
    }
  }
}

