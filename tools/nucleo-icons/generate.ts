type GlyphElementOut =
  | { kind: "path"; d: string }
  | { kind: "circle"; cx: number; cy: number; r: number }
  | { kind: "line"; x1: number; y1: number; x2: number; y2: number };

function num(el: SvgElement, name: string, context: string, fallback?: number): number | null {
  const raw = el.attrs.get(name);
  if (raw === undefined) {
    if (fallback !== undefined) return fallback;
    fail(`${context}: <${el.tag}> missing required attribute "${name}"`);
    return null;
  }
  const n = Number(raw);
  if (!Number.isFinite(n)) {
    fail(`${context}: <${el.tag}> has non-numeric ${name}="${raw}"`);
    return null;
  }
  return n;
}

function elementMatrix(el: SvgElement, context: string): Matrix | null {
  const t = el.attrs.get("transform");
  if (t === undefined) return IDENTITY;
  return parseTransform(t, context);
}

function parsePoints(raw: string, context: string): [number, number][] | null {
  const nums = raw.trim().split(/[\s,]+/).filter(Boolean).map(Number);
  if (nums.length < 4 || nums.length % 2 !== 0 || nums.some((n) => !Number.isFinite(n))) {
    fail(`${context}: unparseable points="${raw}"`);
    return null;
  }
  const pts: [number, number][] = [];
  for (let i = 0; i < nums.length; i += 2) pts.push([nums[i]!, nums[i + 1]!]);
  return pts;
}

/** Rounded (or plain) rect → path d, with the rigid transform baked in. */
function rectToPathD(el: SvgElement, m: Matrix, context: string): string | null {
  const x = num(el, "x", context, 0);
  const y = num(el, "y", context, 0);
  const w = num(el, "width", context);
  const h = num(el, "height", context);
  if (x === null || y === null || w === null || h === null) return null;
  let rx = el.attrs.has("rx") ? num(el, "rx", context) : null;
  let ry = el.attrs.has("ry") ? num(el, "ry", context) : null;
  if (rx === null && ry !== null) rx = ry;
  if (ry === null && rx !== null) ry = rx;
  rx = Math.min(rx ?? 0, w / 2);
  ry = Math.min(ry ?? 0, h / 2);

  const p = (px: number, py: number) => apply(m, px, py).map(fmtNum).join(" ");
  if (rx === 0 || ry === 0) {
    return `M ${p(x, y)} L ${p(x + w, y)} L ${p(x + w, y + h)} L ${p(x, y + h)} Z`;
  }
  const rot = fmtNum(rotationDeg(m));
  const arc = (tx: number, ty: number) => `A ${fmtNum(rx!)} ${fmtNum(ry!)} ${rot} 0 1 ${p(tx, ty)}`;
  return [
    `M ${p(x + rx, y)}`,
    `L ${p(x + w - rx, y)}`,
    arc(x + w, y + ry),
    `L ${p(x + w, y + h - ry)}`,
    arc(x + w - rx, y + h),
    `L ${p(x + rx, y + h)}`,
    arc(x, y + h - ry),
    `L ${p(x, y + ry)}`,
    arc(x + rx, y),
    "Z",
  ].join(" ");
}

/** Ellipse → path d (two arcs), with the rigid transform baked in. */
function ellipseToPathD(el: SvgElement, m: Matrix, context: string): string | null {
  const cx = num(el, "cx", context, 0);
  const cy = num(el, "cy", context, 0);
  const rx = num(el, "rx", context);
  const ry = num(el, "ry", context);
  if (cx === null || cy === null || rx === null || ry === null) return null;
  const rot = fmtNum(rotationDeg(m));
  const p = (px: number, py: number) => apply(m, px, py).map(fmtNum).join(" ");
  const west = p(cx - rx, cy);
  const east = p(cx + rx, cy);
  const r = `${fmtNum(rx)} ${fmtNum(ry)}`;
  return `M ${west} A ${r} ${rot} 1 0 ${east} A ${r} ${rot} 1 0 ${west} Z`;
}

function convertGlyphElement(el: SvgElement, context: string): GlyphElementOut | null {
  for (const [name, value] of el.attrs) {
    if ((name === "stroke" || name === "fill") && isPaintReference(value)) {
      fail(`${context}: unsupported paint reference ${name}="${value}" on <${el.tag}>`);
      return null;
    }
  }
  const m = elementMatrix(el, context);
  if (m === null) return null;
  const hasTransform = el.attrs.has("transform");

  switch (el.tag) {
    case "path": {
      if (hasTransform) {
        fail(
          `${context}: <path> with transform is not supported in glyph data (path data cannot be rewritten)`,
        );
        return null;
      }
      const d = el.attrs.get("d");
      if (!d) {
        fail(`${context}: <path> missing "d"`);
        return null;
      }
      return { kind: "path", d };
    }
    case "circle": {
      const cx = num(el, "cx", context, 0);
      const cy = num(el, "cy", context, 0);
      const r = num(el, "r", context);
      if (cx === null || cy === null || r === null) return null;
      const [tx, ty] = apply(m, cx, cy);
      return {
        kind: "circle",
        cx: Number(fmtNum(tx)),
        cy: Number(fmtNum(ty)),
        r: Number(fmtNum(r)),
      };
    }
    case "line": {
      const x1 = num(el, "x1", context, 0);
      const y1 = num(el, "y1", context, 0);
      const x2 = num(el, "x2", context, 0);
      const y2 = num(el, "y2", context, 0);
      if (x1 === null || y1 === null || x2 === null || y2 === null) return null;
      const [tx1, ty1] = apply(m, x1, y1);
      const [tx2, ty2] = apply(m, x2, y2);
      return {
        kind: "line",
        x1: Number(fmtNum(tx1)),
        y1: Number(fmtNum(ty1)),
        x2: Number(fmtNum(tx2)),
        y2: Number(fmtNum(ty2)),
      };
    }
    case "rect": {
      const d = rectToPathD(el, m, context);
      return d === null ? null : { kind: "path", d };
    }
    case "ellipse": {
      const d = ellipseToPathD(el, m, context);
      return d === null ? null : { kind: "path", d };
    }
    case "polyline":
    case "polygon": {
      const raw = el.attrs.get("points");
      if (!raw) {
        fail(`${context}: <${el.tag}> missing "points"`);
        return null;
      }
      const pts = parsePoints(raw, context);
      if (pts === null) return null;
      const mapped = pts.map(([px, py]) => apply(m, px, py).map(fmtNum).join(" "));
      const d =
        `M ${mapped[0]} ` +
        mapped
          .slice(1)
          .map((pt) => `L ${pt}`)
          .join(" ") +
        (el.tag === "polygon" ? " Z" : "");
      return { kind: "path", d };
    }
    default:
      fail(`${context}: unsupported element <${el.tag}> in glyph data`);
      return null;
  }
}

function emitGlyphElement(el: GlyphElementOut): string {
  switch (el.kind) {
    case "path":
      return `{ kind: "path", d: ${JSON.stringify(el.d)} }`;
    case "circle":
      return `{ kind: "circle", cx: ${el.cx}, cy: ${el.cy}, r: ${el.r} }`;
    case "line":
      return `{ kind: "line", x1: ${el.x1}, y1: ${el.y1}, x2: ${el.x2}, y2: ${el.y2} }`;
  }
}

