// ---------------------------------------------------------------------------
// SVG parsing (tokenizer — corpus SVGs are flat and regular)
// ---------------------------------------------------------------------------

type SvgElement = {
  tag: string;
  /** Attribute name → value, in source order. */
  attrs: Map<string, string>;
};

type ParsedSvg = {
  viewBox: string;
  /** Square viewBox side length (validation guarantees width === height). */
  viewBoxSize: number;
  elements: SvgElement[];
};

const SUPPORTED_CHILD_TAGS = new Set([
  "path",
  "rect",
  "line",
  "polyline",
  "polygon",
  "circle",
  "ellipse",
]);

function parseAttrs(raw: string): Map<string, string> {
  const attrs = new Map<string, string>();
  const re = /([\w:-]+)\s*=\s*"([^"]*)"/g;
  let m: RegExpExecArray | null;
  while ((m = re.exec(raw)) !== null) {
    attrs.set(m[1]!, m[2]!);
  }
  return attrs;
}

/** Parse one SVG's text. Returns null (after recording errors) if unusable. */
function parseSvg(source: string, text: string): ParsedSvg | null {
  const rootMatch = text.match(/<svg\b([^>]*)>/);
  if (!rootMatch) {
    fail(`${source}: no <svg> root element found`);
    return null;
  }
  const rootAttrs = parseAttrs(rootMatch[1]!);
  const viewBox = rootAttrs.get("viewBox");
  if (!viewBox) {
    fail(`${source}: root <svg> has no viewBox`);
    return null;
  }
  const vb = viewBox.trim().split(/[\s,]+/).map(Number);
  if (vb.length !== 4 || vb.some((n) => !Number.isFinite(n))) {
    fail(`${source}: unparseable viewBox "${viewBox}"`);
    return null;
  }
  const [minX, minY, width, height] = vb as [number, number, number, number];
  let ok = true;
  if (minX !== 0 || minY !== 0) {
    fail(`${source}: viewBox origin must be 0 0 (got "${viewBox}")`);
    ok = false;
  }
  if (width !== height) {
    fail(`${source}: non-square viewBox "${viewBox}" (${width}x${height})`);
    ok = false;
  }

  // Every open tag other than the root. Corpus elements are flat (no <g>,
  // no nesting inside shape elements), written as `<path ...></path>`.
  const elements: SvgElement[] = [];
  const tagRe = /<([a-zA-Z][\w:-]*)\b([^>]*?)\/?>/g;
  let m: RegExpExecArray | null;
  while ((m = tagRe.exec(text)) !== null) {
    const tag = m[1]!;
    if (tag === "svg") continue;
    if (!SUPPORTED_CHILD_TAGS.has(tag)) {
      fail(`${source}: unsupported element <${tag}>`);
      ok = false;
      continue;
    }
    elements.push({ tag, attrs: parseAttrs(m[2]!) });
  }
  if (elements.length === 0 && ok) {
    fail(`${source}: no drawable elements found`);
    ok = false;
  }
  return ok ? { viewBox, viewBoxSize: width, elements } : null;
}

// ---------------------------------------------------------------------------
// Number formatting (deterministic, compact)
// ---------------------------------------------------------------------------

function fmtNum(n: number): string {
  const rounded = Math.round(n * 10000) / 10000;
  const clean = Object.is(rounded, -0) ? 0 : rounded;
  return String(clean);
}

// ---------------------------------------------------------------------------
// Transform parsing (translate/rotate only — rigid transforms)
// ---------------------------------------------------------------------------

/** 2x3 affine matrix [a, b, c, d, e, f]: (x,y) → (ax + cy + e, bx + dy + f). */
type Matrix = [number, number, number, number, number, number];

const IDENTITY: Matrix = [1, 0, 0, 1, 0, 0];

function multiply(m1: Matrix, m2: Matrix): Matrix {
  return [
    m1[0] * m2[0] + m1[2] * m2[1],
    m1[1] * m2[0] + m1[3] * m2[1],
    m1[0] * m2[2] + m1[2] * m2[3],
    m1[1] * m2[2] + m1[3] * m2[3],
    m1[0] * m2[4] + m1[2] * m2[5] + m1[4],
    m1[1] * m2[4] + m1[3] * m2[5] + m1[5],
  ];
}

function apply(m: Matrix, x: number, y: number): [number, number] {
  return [m[0] * x + m[2] * y + m[4], m[1] * x + m[3] * y + m[5]];
}

/** Net rotation of a rigid matrix, in degrees (for arc x-axis-rotation). */
function rotationDeg(m: Matrix): number {
  return (Math.atan2(m[1], m[0]) * 180) / Math.PI;
}

/**
 * Parse a transform list into a matrix. Only translate()/rotate() are
 * supported (all the corpus uses); anything else records an error.
 */
function parseTransform(value: string, context: string): Matrix | null {
  let matrix: Matrix = IDENTITY;
  const re = /([a-zA-Z]+)\s*\(([^)]*)\)/g;
  let m: RegExpExecArray | null;
  let matched = false;
  while ((m = re.exec(value)) !== null) {
    matched = true;
    const fn = m[1]!;
    const args = m[2]!.trim().split(/[\s,]+/).filter(Boolean).map(Number);
    if (args.some((n) => !Number.isFinite(n))) {
      fail(`${context}: unparseable transform arguments in "${value}"`);
      return null;
    }
    if (fn === "translate" && (args.length === 1 || args.length === 2)) {
      matrix = multiply(matrix, [1, 0, 0, 1, args[0]!, args[1] ?? 0]);
    } else if (fn === "rotate" && (args.length === 1 || args.length === 3)) {
      const rad = (args[0]! * Math.PI) / 180;
      const rot: Matrix = [Math.cos(rad), Math.sin(rad), -Math.sin(rad), Math.cos(rad), 0, 0];
      if (args.length === 3) {
        const [, cx, cy] = args as [number, number, number];
        matrix = multiply(matrix, [1, 0, 0, 1, cx, cy]);
        matrix = multiply(matrix, rot);
        matrix = multiply(matrix, [1, 0, 0, 1, -cx, -cy]);
      } else {
        matrix = multiply(matrix, rot);
      }
    } else {
      fail(`${context}: unsupported transform function "${fn}(${m[2]})" (only translate/rotate)`);
      return null;
    }
  }
  if (!matched) {
    fail(`${context}: unparseable transform "${value}"`);
    return null;
  }
  return matrix;
}

