/** Resolve the --library <dir> CLI flag, if present. */
function libraryFromArgv(): string | null {
  const argv = process.argv.slice(2);
  for (let i = 0; i < argv.length; i++) {
    const a = argv[i]!;
    if (a === "--library") return argv[i + 1] ?? null;
    if (a.startsWith("--library=")) return a.slice("--library=".length);
  }
  return null;
}

/**
 * The ordered list of candidate library roots. The first that both exists and
 * contains the requested source is used. The vendored copy is always the last
 * resort so CI / library-less machines still regenerate.
 */
function libraryRoots(): string[] {
  const roots: string[] = [];
  const fromArgv = libraryFromArgv();
  if (fromArgv) roots.push(resolve(fromArgv));
  const fromEnv = process.env.NUCLEO_LIBRARY_DIR;
  if (fromEnv) roots.push(resolve(fromEnv));
  roots.push(DEFAULT_LIBRARY_DIR);
  return roots;
}

const LIBRARY_ROOTS = libraryRoots();

/** source path (nucleo_ui/outline/.../18px_check.svg) → vendored basename. */
function vendorName(source: string): string {
  return source.split("/").join("--");
}

