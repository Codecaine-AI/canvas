const DROPPED_ATTRS = new Set(["id", "class"]);

/** data-color / data-name / data-stroke etc — Nucleo metadata, never rendered. */
function isDroppedAttr(name: string): boolean {
  return DROPPED_ATTRS.has(name) || name.startsWith("data-");
}

/** kebab-case SVG presentation attributes → JSX camelCase. */
const JSX_ATTR_NAMES: Record<string, string> = {
  "stroke-width": "strokeWidth",
  "stroke-linecap": "strokeLinecap",
  "stroke-linejoin": "strokeLinejoin",
  "stroke-miterlimit": "strokeMiterlimit",
  "stroke-dasharray": "strokeDasharray",
  "stroke-dashoffset": "strokeDashoffset",
  "stroke-opacity": "strokeOpacity",
  "fill-rule": "fillRule",
  "fill-opacity": "fillOpacity",
  "clip-rule": "clipRule",
};

/** Attributes passed through unchanged (already valid JSX names). */
const PASSTHROUGH_ATTRS = new Set([
  "d",
  "points",
  "x",
  "y",
  "width",
  "height",
  "rx",
  "ry",
  "cx",
  "cy",
  "r",
  "x1",
  "y1",
  "x2",
  "y2",
  "fill",
  "stroke",
  "opacity",
  "transform",
]);

function isPaintReference(value: string): boolean {
  return value.startsWith("url(");
}

// ---------------------------------------------------------------------------
// Output 1: chrome icon components (nucleo/<kebab>.tsx)
// ---------------------------------------------------------------------------

/** AlertTriangleIcon → alert-triangle-icon */
function kebabCase(exportName: string): string {
  return exportName
    .replace(/([a-z0-9])([A-Z])/g, "$1-$2")
    .replace(/([A-Z]+)([A-Z][a-z])/g, "$1-$2")
    .toLowerCase();
}

function emitTsxElement(el: SvgElement, source: string): string | null {
  const parts: string[] = [];
  let ok = true;
  for (const [name, value] of el.attrs) {
    if (isDroppedAttr(name)) continue;
    let outName: string;
    if (PASSTHROUGH_ATTRS.has(name)) {
      outName = JSX_ATTR_NAMES[name] ?? name;
    } else if (name in JSX_ATTR_NAMES) {
      outName = JSX_ATTR_NAMES[name]!;
    } else {
      fail(`${source}: unsupported attribute "${name}" on <${el.tag}>`);
      ok = false;
      continue;
    }
    let outValue = value;
    if ((name === "stroke" || name === "fill") && isPaintReference(value)) {
      fail(`${source}: unsupported paint reference ${name}="${value}" on <${el.tag}>`);
      ok = false;
      continue;
    }
    if (name === "stroke" && value !== "none") outValue = "currentColor";
    if (name === "fill" && value !== "none") outValue = "currentColor";
    parts.push(`${outName}="${outValue}"`);
  }
  if (!ok) return null;
  return `<${el.tag} ${parts.join(" ")} />`;
}

/** Full per-icon .tsx module source for one component. */
function emitTsxModule(exportName: string, svg: ParsedSvg, source: string): string | null {
  const children: string[] = [];
  let ok = true;
  for (const el of svg.elements) {
    const line = emitTsxElement(el, source);
    if (line === null) ok = false;
    else children.push(`      ${line}`);
  }
  if (!ok) return null;
  return [
    GENERATED_HEADER,
    '"use client";',
    "",
    'import type { IconProps } from "../icon-props";',
    "",
    `/** ${source} */`,
    `export function ${exportName}({ className, style }: IconProps) {`,
    "  return (",
    `    <svg viewBox="${svg.viewBox}" className={className} style={style} fill="none" aria-hidden="true">`,
    ...children,
    "    </svg>",
    "  );",
    "}",
    "",
  ].join("\n");
}

